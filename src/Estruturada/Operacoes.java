package Estruturada;

public class Operacoes {

    static public String contador(int i, int f) {
        String s = "";
        for (int c = i; c <= f; c++) {
            s += c + " ";
        }
        return s;
    }

    static public int soma3(int a, int b)
    {
        int s = a + b;
        return s;
    }


}
